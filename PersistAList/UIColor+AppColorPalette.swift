import Foundation
import UIKit

extension UIColor {
    
    static func palPurpleColor() -> UIColor {
        return UIColor(red: 0.604, green: 0.361, blue: 0.706, alpha: 1.00)
    }
    
    static func palOrangeColor() -> UIColor {
        return UIColor (red: 0.894, green: 0.494, blue: 0.188, alpha: 1.00)
    }

}

